## Comparison Operator's

print(5 == 5)
print(5 == 7)
print("Hello" == "Hello")
print("Hello" == "Bye")
print("Hie" == "hie")

print(7 != 5)
print(4 != 4)

print(7 > 3)
print(7 > 9)

print(10 < 20)
print(15 < 14)

print(12 >= 10)
print(17 <= 20)

print(25 >= 25)

# Logical Operator's

print(3 > 2 > 1)
print(3 > 2 < 1)

print(3 > 2 and 2 > 1)
print(3 > 2 and 2 < 1)
print(3 > 2 or 2 > 1)
print(3 > 2 or 2 < 1)
print(3 < 2 or 2 < 1)

print(5 > 3)
print(not(5 > 3))